function switchOfStuff(val) {
  let answer = "";
  // Only change code below this line
  switch (val) {
    case 1:
      answer = 'apple';
      break;
    case 2:
      answer = 'bird';
      break;
    case 3:
      answer = 'cat';
      break;
    default:
      answer = 'stuff';
      break;
    
  }


  // Only change code above this line
  return answer;
}

switchOfStuff(1);
